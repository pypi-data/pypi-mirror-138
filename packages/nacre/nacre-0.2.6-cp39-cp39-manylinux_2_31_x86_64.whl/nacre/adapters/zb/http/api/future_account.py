from typing import Any, Dict, List, Optional

from nautilus_trader.core.correctness import PyCondition

from nacre.adapters.zb.common import format_symbol
from nacre.adapters.zb.http.client import ZbHttpClient


class ZbFutureAccountHttpAPI:

    BASE_ENDPOINT = "/Server/api/v2/"

    def __init__(self, client: ZbHttpClient):
        """
        Initialize a new instance of the ``ZbFutureAccountHttpAPI`` class.

        Parameters
        ----------
        client : ZbHttpClient
            The Binance REST API client.

        """
        PyCondition.not_none(client, "client")

        self.client = client

    async def new_order(
        self,
        symbol: str,
        side: int,
        amount: float,
        price: Optional[float] = None,
        action: Optional[int] = None,
        client_order_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        """
        Submit a new order.

        Parameters
        ----------
        symbol : str
            The symbol for the request.
        side : int
            The order side, 1 for open long; 2 for open short; 3 for close long; 4 for close short
        amount : str
            The order quantity in base asset units for the request.
        price : str, optional
            The order price for the request, optional if action in [11, 12, 31, 52, 52]
        client_order_id : str, optional
            The client order ID for the request. A unique ID among open orders.
            Automatically generated if not provided.
        action : int, optional
            Order Type
            1 limit
            3 IOC
            4 maker only
            5 FOK

        Returns
        -------
        dict[str, Any]

        """
        payload: Dict[str, Any] = {"symbol": format_symbol(symbol), "side": side, "amount": amount}
        if price is not None:
            payload["price"] = price
        if action is not None:
            payload["action"] = action
        if client_order_id is not None:
            payload["clientOrderId"] = client_order_id

        return await self.client.sign_request(
            http_method="POST",
            url_path=self.BASE_ENDPOINT + "trade/order",
            payload=payload,
        )

    async def new_batch_order(  # noqa: C901
        self,
        orders: List[Dict[str, Any]],
    ) -> Dict[str, Any]:
        """
        Submit orders in batch

        Parameters
        ----------
        orders: List[Dict]
            Each order dict as follow
            symbol : str
                The symbol for the request.
            side : int
                The order side, 1 for open long; 2 for open short; 3 for close long; 4 for close short
            amount : str
                The order quantity in base asset units for the request.
            price : str, optional
                The order price for the request, optional if action in [11, 12, 31, 52, 52]
            orderCode: str
                The client order ID for the request. A unique ID among open orders.
                Automatically generated if not provided.
            action : int, optional
                Order Type
                1 limit
                3 IOC
                4 maker only
                5 FOK

        Returns
        -------
        dict[str, Any]

        """
        for order in orders:
            order["symbol"] = format_symbol(order["symbol"])

        payload: Dict[str, Any] = {"orderDatas": orders}

        return await self.client.sign_request(
            http_method="POST",
            url_path=self.BASE_ENDPOINT + "trade/batchOrder",
            payload=payload,
        )

